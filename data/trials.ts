// Filter functions
export const getTrialsByCategory = (category: string) => {
  if (category === 'All') return featuredTrials
  return featuredTrials.filter(trial => trial.category === category)
}

export const getPopularTrials = () => {
  return featuredTrials.filter(trial => trial.popular)
}

export const searchTrials = (query: string) => {
  const lowercaseQuery = query.toLowerCase()
  return featuredTrials.filter(trial => 
    trial.name.toLowerCase().includes(lowercaseQuery) ||
    trial.category.toLowerCase().includes(lowercaseQuery) ||
    trial.description.toLowerCase().includes(lowercaseQuery)
  )
}
