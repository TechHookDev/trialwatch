export const popularTrials = verifiedTrials.filter(t => t.popular)
export const trialsWithFreeTrial = verifiedTrials.filter(t => t.trialDays > 0)
export const featuredTrials = verifiedTrials.slice(0, 12) // First 12 trials as featured

// Helper functions
export function getTrialsByCategory(category: string): Trial[] {
  if (category === 'All') return verifiedTrials
  return verifiedTrials.filter(t => t.category === category)
}

export function getPopularTrials(): Trial[] {
  return verifiedTrials.filter(t => t.popular).slice(0, 6)
}

export function searchTrials(query: string): Trial[] {
  if (!query) return verifiedTrials
  const lowerQuery = query.toLowerCase()
  return verifiedTrials.filter(t => 
    t.name.toLowerCase().includes(lowerQuery) ||
    t.description.toLowerCase().includes(lowerQuery) ||
    t.category.toLowerCase().includes(lowerQuery) ||
    t.features.some(f => f.toLowerCase().includes(lowerQuery))
  )
}

export function getTrialById(id: string): Trial | undefined {
  return verifiedTrials.find(t => t.id === id)
}
